/// 清理文件名中的非法字符，确保生成的文件名在所有主流操作系统上均可安全使用。
///
/// 遍历输入字符串的每个 Unicode 字符：
/// - 保留字母（`A-Z`、`a-z`、中文等 Unicode 字母）、数字、`.`、`-`、`_`。
/// - 将所有其他字符（包括路径分隔符 `/`、`\`，空格，特殊符号等）替换为 `_`。
/// - 最多保留前 100 个字符，超出部分直接截断，避免超出操作系统文件名长度限制。
///
/// # Arguments
///
/// * `name` - 原始文件名字符串（可包含任意 Unicode 字符）。
///
/// # Returns
///
/// * `String` - 清理后的安全文件名，长度不超过 100 个字符，
///   仅包含字母、数字、`.`、`-`、`_` 四类字符。
pub fn sanitize_filename(name: &str) -> String {
    name.chars()
        .take(100)
        .map(|c| {
            if c.is_alphanumeric() || c == '.' || c == '-' || c == '_' {
                c
            } else {
                '_'
            }
        })
        .collect()
}
